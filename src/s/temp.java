public class temp {
   String f_number, from, to, type, time, p_name, f_date,IMAGE;

    public temp(String f_number, String from, String to, String type, String time, String p_name, String f_date, String IMAGE) {
        this.f_number = f_number;
        this.from = from;
        this.to = to;
        this.type = type;
        this.time = time;
        this.p_name = p_name;
        this.f_date = f_date;
        this.IMAGE = IMAGE;
    }

    public String getF_number() {
        return f_number;
    }

    public String getFrom() {
        return from;
    }

    public String getTo() {
        return to;
    }

    public String getType() {
        return type;
    }

    public String getTime() {
        return time;
    }

    public String getP_name() {
        return p_name;
    }

    public String getF_date() {
        return f_date;
    }

    public String getIMAGE() {
        return IMAGE;
    }

    public void setF_number(String f_number) {
        this.f_number = f_number;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public void setP_name(String p_name) {
        this.p_name = p_name;
    }

    public void setF_date(String f_date) {
        this.f_date = f_date;
    }

    public void setIMAGE(String IMAGE) {
        this.IMAGE = IMAGE;
    }
   
 
}
